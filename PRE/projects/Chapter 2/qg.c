#include<stdio.h>
#include<conio.h>
#include<math.h>
int main()
{
    int a;
    printf("Enter the Angle : ");
    scanf("%d",&a);
    printf("The Trignometric Ratios :\n\t");
    printf("The sin Ratio : %f\n\t",sin(a));
    printf("The cos Ratio : %f\n\t",cos(a));
    printf("The tan Ratio : %f\n\t",tan(a));
    printf("The sinh Ratio : %f\n\t",sinh(a));
    printf("The cosh Ratio : %f\n\t",cosh(a));
    printf("The tanh Ratio : %f\n\t",tanh(a));
    return 0;
}
